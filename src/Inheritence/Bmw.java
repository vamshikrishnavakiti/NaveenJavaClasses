package Inheritence;

public class Bmw extends car {

	//Method Overriding:- When two classes has a same method with same name and zero arguments it is 
	// called method overriding.
	public void start() {
		System.out.println("Auto Start");
	}
	public void GPS() {
		System.out.println("BMW----GPS");
	}
	public void theftsafty() {
		System.out.println("BMW___Theftsafty");
	}
public void doors() {
	System.out.println("BMW---Doors");
}
}
