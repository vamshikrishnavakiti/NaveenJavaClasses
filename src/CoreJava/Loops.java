package CoreJava;

public class Loops {

	public static void main(String[] args) {
// While loop
		int i =1;
		while(i<=10) {
			System.out.println(i);
			i = i+1;
		}
		System.out.println("**********************************");
		// For loop
		for(int j=0; j<=100; j++) {
			System.out.println(j);
		}
	}

}
