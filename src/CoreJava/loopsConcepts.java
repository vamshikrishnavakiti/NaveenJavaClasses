package CoreJava;

public class loopsConcepts {

	public static void main(String[] args) {
//while loops
		int i=10;
		while(i>=1) {
			System.out.println(i);
			i=i-1;
		}
		
		System.out.println("*******************");
		
		int j=1;
		for (j=1; j<=10; j++) {
			System.out.println(j);
		}
		
		
	}

}
